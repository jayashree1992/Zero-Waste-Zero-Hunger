    public void populateStockTable(){
        DefaultTableModel dtm = (DefaultTableModel) edibleInventoryTable.getModel();
       dtm.setRowCount(0);

        for(FoodItem fi : ngoEnterprise.getInventory().getEdibleInventory()){
                Object row[] = new Object[3];
                row[0] = fi;
                row[1] = fi.getQuantity();
                row[2] = fi.getTestResult().getBestBefore();
                
                dtm.addRow(row);
        }
        
        
    }
    
    
    public void populateDistributedTable(){
        DefaultTableModel dtm = (DefaultTableModel) peopleFedInventoryTable.getModel();
        dtm.setRowCount(0);

        for(Packet p : ngoEnterprise.getInventory().getDistributedInventory()){
                Object row[] = new Object[3];
                row[0] = p;
                row[1] = p.getFoodItem().getQuantity();
                row[2] = p.getDate();
               
                
                dtm.addRow(row);
        }
        
        
    }
    
