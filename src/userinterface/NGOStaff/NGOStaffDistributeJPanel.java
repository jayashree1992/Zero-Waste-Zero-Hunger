    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        DefaultTableModel dtm = (DefaultTableModel) edibleInventoryTable.getModel();
        int selectedRowIndex = edibleInventoryTable.getSelectedRow();

        if (selectedRowIndex < 0) {
            JOptionPane.showMessageDialog(null, "Please select food items to distribute");
            return;
        }
        
        FoodItem foodItem = (FoodItem) dtm.getValueAt(selectedRowIndex, 0);
        
        int quantity = (Integer) foodQuantity.getValue();
        
        int availableQuantity = foodItem.getQuantity();
        
        Date bestBefore = foodItem.getTestResult().getBestBefore();
        Date today = new Date();
      
         
        if(bestBefore.before(today)){
            
            foodItem.getTestResult().setMessage("Expired");
            
            ngoEnterprise.getInventory().getWasteInventory().add(foodItem);
            ngoEnterprise.getInventory().getEdibleInventory().remove(foodItem);
            JOptionPane.showMessageDialog(null, "Items have expired and have been moved to waste Inventory ");
            populateStockTable();
            return;
        }
        
        
        if(quantity<=0){
            JOptionPane.showMessageDialog(null, "Enter quantity greater than 0");
            return;
        }
        
        if(quantity>availableQuantity){
            JOptionPane.showMessageDialog(null, "Only " + availableQuantity + " items left in stock");
            return;
        }
        
         int peoplefed = (Integer) peopleFed.getValue();
         
        if(peoplefed<=0){
            JOptionPane.showMessageDialog(null, "People fed cannot be negative or 0");
            return;
        }
       
       
        
        FoodItem distributedItem = null;
        
        try {
            distributedItem = (FoodItem) foodItem.clone();
        } catch (CloneNotSupportedException ex) {
            System.out.print("CloneNotSupportedException in NGOStaffDistributeJPanel");
        }
        
         foodItem.setQuantity(availableQuantity-quantity);
         distributedItem.setQuantity(quantity);
         
          Packet packet = new Packet();
          
          packet.setFoodItem(distributedItem);
          packet.setDate(new Date());
          packet.setPeopleFed(peoplefed);
          
        
        if(quantity == availableQuantity){
            ngoEnterprise.getInventory().getEdibleInventory().remove(foodItem);

        }
         
        
         
        ngoEnterprise.getInventory().getDistributedInventory().add(packet);
        populateStockTable();
        populateDistributedTable();
        
        
       // NGOEnterprise e = (NGOEnterprise) enterprise;
        
        //e.getInventory().getDistributedInventory().add();
    }//GEN-LAST:event_jButton2ActionPerformed

