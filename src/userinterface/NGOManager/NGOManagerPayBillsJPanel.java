    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        DefaultTableModel dtm = (DefaultTableModel) payBillsTable.getModel();

        int selectedRowIndex = payBillsTable.getSelectedRow();

        if (selectedRowIndex < 0) {
            JOptionPane.showMessageDialog(null, "Select a request for paying bill");
            return;
        }
        WorkRequest wr = (WorkRequest) dtm.getValueAt(selectedRowIndex, 0);
        BillingRequest b = (BillingRequest) wr;
        b.setBillPaid(true);
        b.setStatus("Bill Paid");
        payBillsTable();

    }//GEN-LAST:event_jButton1ActionPerformed

