    private void payBillsTable() {

        DefaultTableModel dtm = (DefaultTableModel) payBillsTable.getModel();
        dtm.setRowCount(0);

        for (WorkRequest wr : organization.getWorkQueue().getWorkRequestList()) {

            if (wr instanceof BillingRequest) {
                BillingRequest b = (BillingRequest) wr;

                if (b.getDeliveryrequest().getStatus().equals("Delivery Completed")) {
                    Object row[] = new Object[5];
                    row[0] = b;
                    row[1] = b.getDeliveryrequest().getDeliveryEnterprise();
                    row[2] = b.getInvoiceDate();
                    row[3] = b.getDeliveryrequest().getCost();
                    row[4] = b.isBillPaid() ? "Yes" : "No";

                    dtm.addRow(row);
                }

            }
        }
    }

